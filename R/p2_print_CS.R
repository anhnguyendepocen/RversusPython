#         C                                S 
#          C                      S       S
#           C       C              S S S
#             C C C                   
#  
# **************************************************
#     Computer Science is Cool Stuff!!

print.noquote("************************************************")
print.noquote("")
print.noquote("            C C C                 S S S")
print.noquote("          C       C              S     S")
print.noquote("         C                      S")
print.noquote("        C                        S")
print.noquote("        C                         S S S")
print.noquote("        C                                S")
print.noquote("         C                      S       S")
print.noquote("          C       C              S S S")
print.noquote("            C C C")
print.noquote("")
print.noquote("**************************************************")
print.noquote("    Computer Science is Cool Stuff!!")

