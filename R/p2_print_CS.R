# Description: Write a program that prints out "CS!" in large block letters inside 
#              a border of *** followed by two blank lines then the message 
#              "Computer Science is Cool Stuf" as shown below:
# ************************************************
# 
#             C C C                 S S S
#           C       C              S     S
#          C                      S
#         C                        S
